import Foundation

struct TrendingMovieResponse: Codable {
    let page: Int
    let results: [MovieResult]
    let totalPages, totalResults: Int
    
    enum CodingKeys: String, CodingKey {
        case page, results
        case totalPages = "total_pages"
        case totalResults = "total_results"
    }
}
